function getAreaName(areaCode){
    switch (areaCode){
        case 1:
            return "서울";
            break;
        case 2:
            return "인천";
            break;
        case 31:
            return "경기";
            break;
        case 32:
            return "강원";
            break;
        case 33:
            return "충북";
            break;
        case 34:
            return "충남";
            break;
        case 35:
            return "경북";
            break;
        case 36:
            return "경남";
            break;
        case 37:
            return "전북";
            break;
        case 38:
            return "전남";
            break;
        case 39:
            return "제주";
            break;

    }
}